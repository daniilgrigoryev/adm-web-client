        this.docsPetition.inspUtvKod = data.inspKod;
        this.docsPetition.inspUtvName = data.inspName;
        this.docsPetition.inspUtvDolz = data.inspDolz;
        this.docsPetition.inspUtvRang = data.inspRang;
        this.inspUtvModal.visible = false;
        this.inspUtvModal.inspUtvList = null;
        this.store();
      },

      async clearInspSost() {
        this.docsPetition.inspSostId = null;
        this.docsPetition.inspSostKod = null;
        this.docsPetition.inspSostName = null;
        this.docsPetition.inspSostDolz = null;
        this.docsPetition.inspSostRang = null;
        await this.store();
      },
      async clearOrganSost() {
        this.docsPetition.organSostId = null;
        this.docsPetition.organSostKod = null;
        this.docsPetition.organSostName = null;
        await this.store();
      },
      async clearInspUtv() {
        this.docsPetition.inspUtvId = null;
        this.docsPetition.inspUtvKod = null;
        this.docsPetition.inspUtvName = null;
        this.docsPetition.inspUtvDolz = null;
        this.docsPetition.inspUtvRang = null;
        await this.store();
      },

      store() {
        return RequestApi.prepareData({
          method: 'store',
          params: {
            data: this.docsPetition
          }
        });
      },
      async save() {
        await this.store();
        let eventResponse = await RequestApi.prepareData({
          method: 'update'
        });
        if (eventResponse.response) {
          let error = JSON.parse(eventResponse.response).error;
          this.$store.dispatch('errors/changeContent', {title: error.errorMsg, desc: error.errorDesc,});
        } else {
          this.getPrev();
        }
      },
      async getPlaceSost() {
        let eventResponse = await RequestApi.prepareData({
          method: 'getPlaceSostCID'
        });
        let cid = JSON.parse(eventResponse.response).data;

        formStack.toNext({
          module: this.$store.state.placeViewEdit,
          vm: this,
          notRemoved: false,
          withCreate: false,
          cid: cid,
          params: {
            type: 'placeSost'
          }
        });
      },
      getPrev() {
        try {
          formStack.toPrev({
            vm: this
          });
        } catch (e) {
          this.$store.dispatch('errors/changeContent', {title: e.message,});
        }
      },
    },
  }
</script>
